const areaOfTriangleFormula = (base, height) => {
	return (1 / 2) * Number(base) * Number(height);
};

const perimeterOfTriangleFormula = (sideA, sideB, sideC) => {
	return Number(sideA) + Number(sideB) + Number(sideC);
};

const areaOfParallelogram = (base, height) => {
	return Number(base) * Number(height);
};

const perimeterofParallelogram = (side, base) => {
	return 2 * (Number(side) + Number(base));
};
